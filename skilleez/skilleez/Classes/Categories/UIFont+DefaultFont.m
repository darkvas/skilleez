#import "UIFont+DefaultFont.h"

@implementation UIFont (DefaultFont)

+ (UIFont *)getDKCrayonFontWithSize:(CGFloat)size
{
    return [UIFont fontWithName:@"DKCrayonCrumble" size:size];
}

@end
